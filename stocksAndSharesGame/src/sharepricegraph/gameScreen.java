    public static void performBuy(int companyNum) {
        if (totalMoney >= stockValues[companyNum]) {
            totalMoney -= stockValues[companyNum];
            numOfStocks[companyNum]++;
            totalStockCount++;
            totalValueOfOwnedStocks += stockValues[companyNum];
            valueOfOwendStocks[companyNum] += stockValues[companyNum];

            moneyToT.setText(String.valueOf(totalMoney));
            totalStocksNumber.setText(String.valueOf(totalStockCount));
            totalStockValue.setText(String.valueOf(totalValueOfOwnedStocks));
        }
        inableDisableBuyButtons();
    }

    public static void performSell(int companyNum) {
        if (numOfStocks[companyNum] > 0) {
            totalMoney += stockValues[companyNum];
            numOfStocks[companyNum]--;
            totalStockCount--;
            totalValueOfOwnedStocks -= stockValues[companyNum];
            valueOfOwendStocks[companyNum] -= stockValues[companyNum];

            moneyToT.setText(String.valueOf(totalMoney));
            totalStocksNumber.setText(String.valueOf(totalStockCount));
            totalStockValue.setText(String.valueOf(totalValueOfOwnedStocks));
        }
        inableDisableBuyButtons();
    }

    public static void UpdateStockValues(int appoolValue, int microsooftValue, int amazoonValue) {
        appoolStockValue.setText(String.valueOf(appoolValue));
        microsooftStockValue.setText(String.valueOf(microsooftValue));
        amazoonStockValue.setText(String.valueOf(amazoonValue));

        valueOfOwendStocks[0] = appoolValue*numOfStocks[0];
        yourAppoolStocksValue.setText(String.valueOf(valueOfOwendStocks[0]));
        valueOfOwendStocks[1] = microsooftValue* numOfStocks[1];
        yourMicrosooftStocksValue.setText(String.valueOf(valueOfOwendStocks[1]));
        valueOfOwendStocks[2] = amazoonValue* numOfStocks[2];
        yourAmazoonStocksValue.setText(String.valueOf(valueOfOwendStocks[2]));

        totalValueOfOwnedStocks = valueOfOwendStocks[0] + valueOfOwendStocks[1] + valueOfOwendStocks[2];
        totalStockValue.setText(String.valueOf(totalValueOfOwnedStocks));

        inableDisableBuyButtons();
    }

    public static void inableDisableBuyButtons() {
        if (stockValues[0] > totalMoney || stockValues[0] == 0) {
            buyAppool.setVisible(false);
        } else {
            buyAppool.setVisible(true);
        }
        if (stockValues[1] > totalMoney || stockValues[1] == 0) {
            buyMicrosooft.setVisible(false);
        } else {
            buyMicrosooft.setVisible(true);
        }
        if (stockValues[2] > totalMoney || stockValues[2] == 0) {
            buyAmazoon.setVisible(false);
        } else {
            buyAmazoon.setVisible(true);
        }
        
        if(numOfStocks[0] < 1){
            sellAppool.setVisible(false);
        }else{
            sellAppool.setVisible(true);
        }
        if(numOfStocks[1] < 1){
            sellMicrosooft.setVisible(false);
        }else{
            sellMicrosooft.setVisible(true);
        }
        if(numOfStocks[2] < 1){
            sellAmazoon.setVisible(false);
        }else{
            sellAmazoon.setVisible(true);
        }
    }

